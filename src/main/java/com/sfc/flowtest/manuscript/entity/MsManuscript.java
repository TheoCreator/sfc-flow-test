package com.sfc.flowtest.manuscript.entity;

import java.time.LocalDateTime;

public class MsManuscript {
    private Long id;
    private String title;
    private String body;
    private String status;
    private Integer rejectReviewLevel;
    private LocalDateTime createdAt;
    private LocalDateTime updatedAt;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public Integer getRejectReviewLevel() {
        return rejectReviewLevel;
    }

    public void setRejectReviewLevel(Integer rejectReviewLevel) {
        this.rejectReviewLevel = rejectReviewLevel;
    }

    public LocalDateTime getCreatedAt() {
        return createdAt;
    }

    public void setCreatedAt(LocalDateTime createdAt) {
        this.createdAt = createdAt;
    }

    public LocalDateTime getUpdatedAt() {
        return updatedAt;
    }

    public void setUpdatedAt(LocalDateTime updatedAt) {
        this.updatedAt = updatedAt;
    }
}
